public class Price {
    private int valuePrice;

    public Price(){
        this.valuePrice = valuePrice;
    }

    public int getValuePrice() {
        return valuePrice;
    }

    public void setValuePrice(int valuePrice) {
        this.valuePrice = valuePrice;
    }

    @Override
    public String toString() {
        return "Price{" +
                "valuePrice=" + valuePrice +
                '}';
    }
}
